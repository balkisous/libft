#include"libft.h"
#include<stdio.h>

void	ft_bzero(void *s, size_t n)
{
	int i;
	char *t;

	i = 0;
	t = (char *)s;
	while(n > 0)
	{
		t[i] = '\0';
		n--;
		i++;	
	}
}
