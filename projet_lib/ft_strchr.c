char *ft_strchr(const char* s, int c)
{
	
	int i;
	char d;
	char *t;

	d = (char)c;
	t = (char *)s;
	i = 0;
	while (t[i] != '\0')
	{
		if (t[i] == d)
		{
			printf("d == t\n");
			return(&t[i]);
		}
		i++;
	}
	return(0);
}
